//获取省级所辖城市
function getCities(provinceIndex)
{
	var n = 0;
	var cities = new Array();
	for (var i in ja){
		if (provinceIndex == i.substr(0,2) && i != (provinceIndex + '00'))
		{
			cities[n] = ja[i];
			n++;
		}	
	}
	return cities;
}