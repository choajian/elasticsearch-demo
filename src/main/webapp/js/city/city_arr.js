function getProvince(city){
	for (var i in ja){
		if (city == ja[i] && '00' != i.substr(3,4))
		{
			return i.substr(0,2) + ',' + ja[i.substr(0,2)+'00'];
		}	
	}
	return '';
}

// 获取省下标
function getProvinceIndex(province)
{
	for (var i in ja){
		if (province == ja[i] && '00' == i.substr(3,4))
		{
			return i.substr(0,2);
		}	
	}
	return '';
}

